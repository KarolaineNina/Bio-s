            <h2><a>Para que serve?</a></h2>
            <p>Uma placa solar fotovoltaica é um dispositivo que converte a energia da luz solar diretamente em eletricidade por meio do chamado efeito fotovoltaico. Esse efeito foi descoberto em 1839 pelo físico francês Alexandre-Edmond Becquerel e consiste na geração de uma diferença de potencial elétrico em um material quando exposto à luz solar.</p>

            <p>A placa solar fotovoltaica é composta por várias células solares interligadas, que são feitas de materiais semicondutores, como o silício. Quando a luz solar atinge essas células, ela é absorvida e os elétrons no material semicondutor são excitados, gerando uma corrente elétrica.</p>

            <p>As células solares são agrupadas em módulos, que são conectados a um inversor solar, responsável por converter a corrente contínua gerada pelas células solares em corrente alternada, que pode ser utilizada pelos dispositivos elétricos.</p>

            <p>As placas solares fotovoltaicas são uma das tecnologias mais comuns e populares para a geração de energia solar, sendo utilizadas em sistemas residenciais, comerciais e industriais. Elas são uma forma limpa, sustentável e econômica de produzir eletricidade a partir da luz solar, e podem ajudar a reduzir a dependência de combustíveis fósseis e a emissão de gases de efeito estufa.</p>
